/**
 * Definition for singly-linked list.
 * public class ListNode {
 * int val;
 * ListNode next;
 * ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public ListNode reverseKGroup(ListNode head, int k) {
        if (head == null || head.next == null || k <= 1) {
            return head;
        }
        int count = 0;
        ListNode index = head;
        ListNode pre = null;
        ListNode current;

        while (index != null && ++count <= k) {
            index = index.next;
        }
        if (count < k) {
            return head;
        }
        count = 0;
        index = head;
        while (index != null && ++count <= k) {
            current = index;
            index = index.next;
            current.next = pre;
            pre = current;
        }
        head.next = reverseKGroup(index, k);
        return pre;
    }
}
